// 获取SVG图标的函数
function getSvgIcon(iconName, className = '') {
    const icon = svgIcons[iconName] || '';
    if (!icon) return '';
    
    // 如果提供了类名，插入到SVG标签中
    if (className) {
        return icon.replace('<svg', `<svg class="${className}"`);
    }
    return icon;
}

// 将SVG图标添加到DOM元素
function insertSvgIcon(element, iconName, className = '') {
    if (!element) return;
    element.innerHTML = getSvgIcon(iconName, className);
}

// 为页面上所有有data-icon属性的元素添加图标
function initIcons() {
    const iconElements = document.querySelectorAll('[data-icon]');
    
    iconElements.forEach(el => {
        const iconName = el.getAttribute('data-icon');
        const className = el.getAttribute('data-icon-class') || '';
        insertSvgIcon(el, iconName, className);
    });
}

// 导出函数供其他脚本使用
window.svgIcons = {
    get: getSvgIcon,
    insert: insertSvgIcon,
    init: initIcons,
}
